
/// There are 4 types API static methods in "Promise"

// 1)  ------- Promise.all() ----
// -> Fullfills when all promises in the iterable are fulfilled, reject immediately if any prmise rejects.


// 2) ------- Promise.allSettled() -----
// -> Wait for all promises to settle (either fulfill or reject) and returns an array if their results.


// 3) -------- Promise.race() --------
// -> This method returns a promise that settles as soon as any of the promises in the array settles(either resolved or rejected);


// 4) ----- Promise.any() -------
// -> THis method returns a promise that resoolves as soon as any of the promises succeeds (resolves )


